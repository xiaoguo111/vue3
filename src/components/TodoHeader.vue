<template>
  <header class="header">
    <h1>todos</h1>
    <input id="toggle-all" class="toggle-all" type="checkbox" >
    <label for="toggle-all"></label> 
    <!-- label 可以关联一个表单标签 -->
    <input
      class="new-todo"
      placeholder="输入任务名称-回车确认"
      autofocus 
      v-model="msg"
      @keydown.enter="add"
    />
  </header>
</template>

<script>
export default {
  data () {
    return {
      msg:''
    }
  },
 methods: {
  add(){
    this.$emit('add',this.msg);
    this.msg=''
  }
 }
}
</script>